void main(List<String> args) {
  print(10 + 10);
  print(10 - 10);
  print(10 / 10); // double
  print(10 ~/ 10); // int
  print(10 * 10);
  print(10 % 3);

  var val = 10;
  print(val++); // print -> add
  print(val--); // print -> add
  print(++val); // subtract -> print
  print(--val); // subtract -> print
}
